    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        // TODO add your handling code here:
                                                 

    // Validar instructor cargado
    if (NSS_instructor.getText().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this,
                "No se encontró un instructor asignado a la matrícula.",
                "Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Obtener datos
    String matricula = campo_filtro.getText();
    int cantidad = (Integer) spnr_cantidad.getValue();

    // Validar cantidad
    if (cantidad <= 0) {
        javax.swing.JOptionPane.showMessageDialog(this,
                "La cantidad de kilómetros debe ser mayor a 0.",
                "Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Llamar procedimiento
    boolean ok = new ProceduresDAO()
            .registrarIncrementoKilometraje(matricula, cantidad);

    if (ok) {
        javax.swing.JOptionPane.showMessageDialog(this,
                "Kilometraje actualizado correctamente.");

        generarDatosAutos(); // refrescar tabla
    } else {
        javax.swing.JOptionPane.showMessageDialog(this,
                "Error al registrar el kilometraje.",
                "Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
    }


        
    }//GEN-LAST:event_btn_guardarActionPerformed


    public void llenar_instructor(){
        String matricula = campo_filtro.getText();
        Instructor i = new InstructorDAO().mostrarInstructorMatricula(matricula);
        if(i==null){
            NSS_instructor.setText("");
        nombre_instructor.setText("");
        }else{
        NSS_instructor.setText(i.getNSS());
        nombre_instructor.setText(i.getNombre());
        }
    }
    
    public boolean campoLleno(){
        if(campo_filtro.getText().length()==6){
            btn_guardar.setEnabled(true);
            llenar_instructor();
            
            return true;
        }else {
            btn_guardar.setEnabled(false);
            llenar_instructor();
            
            return false;
        }
    }
    
    
    public void generarDatosAutos() {

    // Convertir filtro a mayúsculas
    
    
    campo_filtro.setText(campo_filtro.getText().toUpperCase());

    String[] columnas = {"Matricula", "Modelo", "Marca", "Asignado", "Kilometraje"};

    // Obtener autos filtrados SOLO por matricula
        ArrayList<Auto> autosLista = new AutoDAO()
            .mostrarAutosFiltro("matricula", campo_filtro.getText());

    // Lista para llenar la tabla
    ArrayList<Object[]> autosParaMostrar = new ArrayList<>();

    for (Auto e : autosLista) {
        autosParaMostrar.add(new Object[]{
            e.getMatricula(),
            e.getModelo(),
            e.getMarca(),
            e.isAsignado(),
            e.getKilometraje()
        });
    }

    // Modelo sin edición
        DefaultTableModel modeloAutos =
            new DefaultTableModel(autosParaMostrar.toArray(new Object[0][]), columnas) {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };

    tabla_autos.setModel(modeloAutos);
    tabla_autos.setRowHeight(26);
}



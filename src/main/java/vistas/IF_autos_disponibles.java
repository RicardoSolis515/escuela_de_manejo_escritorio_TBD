    JScrollPane sp;
    public IF_autos_disponibles(JScrollPane sp) {
        initComponents();
        
        int x = new FunctionsDAO().cantidadVehiculosDisponibles();
        this.sp = sp;
        if(x==0){
            txt_cantidad_de_autos.setText("No hay vehiculos disponibles");
            txt_segerencia.setText("Se deben comprar vehiculos de repuesto o para agregar instructores");
        }else if(x<3){
            txt_cantidad_de_autos.setText("Se cuentan con " + x + " vehiculos");
            txt_segerencia.setText("Se recomienda comprar mas para agregar instructores");
        }else if(x<7){
            txt_cantidad_de_autos.setText("Se cuentan con " + x + " vehiculos");
            txt_segerencia.setText("Buena cantidad para repuesto y agregar instructores");
        }else{
            txt_cantidad_de_autos.setText("Se cuentan con " + x + " vehiculos");
            txt_segerencia.setText("La cantidad de vehiculos es por encima de la recomendada");
        }
        
        aplicarColores();
    }
    
    

    private void generarTabla() {

        ArrayList<Historial> lista = new ViewsDAO().obtenerVistaHistorialCompletoFiltro(campo_filtro.getText());
        
    String[] columnas = {
        "ID", "Fecha Cambio", "NSS", "Nombre Completo",
        "Vehículo Anterior", "Marca Anterior", "Modelo Anterior",
        "Vehículo Nuevo", "Marca Nueva", "Modelo Nuevo"
    };

    javax.swing.table.DefaultTableModel modelo =
            new javax.swing.table.DefaultTableModel(null, columnas) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false; // No permitir edición
        }
    };

    for (Historial h : lista) {

        String nombreCompleto = h.getNombre() + " " +
                                h.getApellidopaterno() + " " +
                                h.getApellidomaterno();

        Object[] fila = new Object[]{
            h.getId(),
            h.getFecha_cambio(),
            h.getNss(),
            nombreCompleto,
            h.getVehiculo_anterior(),
            h.getMarca_anterior(),
            h.getModelo_anterior(),
            h.getVehiculo_nuevo(),
            h.getMarca_nueva(),
            h.getModelo_nueva()
        };

        modelo.addRow(fila);
    }

    tabla_historial.setModel(modelo);
    tabla_historial.setRowHeight(28);
}

    

